#include "so_long.h"

void	ft_check_ber(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	if (str[i] != 'r' && str[i - 1] != 'e' && str[i - 2] != 'b'
		&& str[i - 3] != '.' && str[i - 4] == '/' && i > 4)
	{
		if (!write(1, "Error format\n", 12))
			exit (0);
		exit (0);
	}
}
